package br.com.educ.model;

import java.util.Date;

public class MortoCoordenador {

	private int idMortoCoordenador;
	private String nomeMortoCoordenador;
	private String rgMortoCoordenador;
	private String cpfMortoCoordenador;
	private char generoMortoCoordenador;
	private Date dataNascimentoMortoCoordenador;
	private String emailMortoCoordenador;
	private String senhaMortoCoordenador;
	private String telefoneMortoCoordenador;
	private Date dataFimMortoCoordenador;

	public int getIdMortoCoordenador() {
		return idMortoCoordenador;
	}

	public void setIdMortoCoordenador(int idMortoCoordenador) {
		this.idMortoCoordenador = idMortoCoordenador;
	}

	public String getNomeMortoCoordenador() {
		return nomeMortoCoordenador;
	}

	public void setNomeMortoCoordenador(String nomeMortoCoordenador) {
		this.nomeMortoCoordenador = nomeMortoCoordenador;
	}

	public String getRgMortoCoordenador() {
		return rgMortoCoordenador;
	}

	public void setRgMortoCoordenador(String rgMortoCoordenador) {
		this.rgMortoCoordenador = rgMortoCoordenador;
	}

	public String getCpfMortoCoordenador() {
		return cpfMortoCoordenador;
	}

	public void setCpfMortoCoordenador(String cpfMortoCoordenador) {
		this.cpfMortoCoordenador = cpfMortoCoordenador;
	}

	public char getGeneroMortoCoordenador() {
		return generoMortoCoordenador;
	}

	public void setGeneroMortoCoordenador(char generoMortoCoordenador) {
		this.generoMortoCoordenador = generoMortoCoordenador;
	}

	public Date getDataNascimentoMortoCoordenador() {
		return dataNascimentoMortoCoordenador;
	}

	public void setDataNascimentoMortoCoordenador(
			Date dataNascimentoMortoCoordenador) {
		this.dataNascimentoMortoCoordenador = dataNascimentoMortoCoordenador;
	}

	public String getEmailMortoCoordenador() {
		return emailMortoCoordenador;
	}

	public void setEmailMortoCoordenador(String emailMortoCoordenador) {
		this.emailMortoCoordenador = emailMortoCoordenador;
	}

	public String getSenhaMortoCoordenador() {
		return senhaMortoCoordenador;
	}

	public void setSenhaMortoCoordenador(String senhaMortoCoordenador) {
		this.senhaMortoCoordenador = senhaMortoCoordenador;
	}

	public String getTelefoneMortoCoordenador() {
		return telefoneMortoCoordenador;
	}

	public void setTelefoneMortoCoordenador(String telefoneMortoCoordenador) {
		this.telefoneMortoCoordenador = telefoneMortoCoordenador;
	}

	public Date getDataFimMortoCoordenador() {
		return dataFimMortoCoordenador;
	}

	public void setDataFimMortoCoordenador(Date dataFimMortoCoordenador) {
		this.dataFimMortoCoordenador = dataFimMortoCoordenador;
	}
}